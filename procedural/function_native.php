mysqli_connect(): Establishes a connection to a MySQL database.
mysqli_query(): Executes an SQL query.
mysqli_fetch_assoc(): Retrieves a row of results as an associative array.
mysqli_close(): Closes the connection to the database.

Security:

password_hash(): Creates a secure hash for a password.
password_verify(): Checks if a password matches its hash.

Sessions and Cookies:

session_start(): Starts a PHP session.
setcookie(): Sets a cookie.

User Input Validation:

filter_var(): Validates and filters data based on the specified type.
htmlspecialchars(): Converts special characters to HTML entities to prevent XSS attacks.

URL Handling:

parse_url(): Parses a URL into its components.
http_build_query(): Generates a query string from an associative array.

File Handling:

glob(): Returns a list of file paths matching a pattern.
scandir(): Reads the contents of a directory.

JSON Manipulation:

json_encode(): Converts a data structure to JSON.
json_decode(): Converts a JSON string to a PHP data structure.

